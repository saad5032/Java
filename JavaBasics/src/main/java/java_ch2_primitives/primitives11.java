package java_ch2_primitives;

// boolean : either true or false
public class primitives11 {
public static void main(String[] args) {
    // Developers will often use the word, is, as a prefix for a boolean variable name. 
    // This creates a name that seems to ask a question, which makes reading the code more intuitive. But other prefixes 
    // can be just as valid.

Boolean isCustomerOverTwentyOne = true; 
System.out.println(isCustomerOverTwentyOne);
}
}
